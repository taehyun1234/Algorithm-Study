#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

/*
왕비를 피해 일곱 난쟁이들과 함께 평화롭게 생활하고 있던 백설공주에게 위기가 찾아왔다. 일과를 마치고 돌아온 난쟁이가 일곱 명이 아닌 아홉 명이었던 것이다.

아홉 명의 난쟁이는 모두 자신이 "백설 공주와 일곱 난쟁이"의 주인공이라고 주장했다. 뛰어난 수학적 직관력을 가지고 있던 백설공주는, 다행스럽게도 일곱 난쟁이의 키의 합이 100이 됨을 기억해 냈다.

아홉 난쟁이의 키가 주어졌을 때, 백설공주를 도와 일곱 난쟁이를 찾는 프로그램을 작성하시오.
*/

int main()
{
	vector<int> height;
	vector<int> answer;

	int sum = 0;

	for (int i = 0; i < 9; i++)
	{
		int tmp = 0;
		cin >> tmp;
		sum += tmp;
		height.push_back(tmp);
	}

	// 9개중 7개를 뽑아서 7개 다 더한 값이 100이 되도록 한다. -> 9개를 더한 다음에 2개를 뺐을 때 100이 되도록 하면 된다.

	int eraseFirst;
	int eraseSecond;
	bool answerAll = false;

	for (int i = 0; i < 9; i++)
	{
		for (int j = 0; j < 9; j++)
		{
			if (i == j)
			{
				continue;
			}
			else
			{
				if (sum - height[i] - height[j] == 100)
				{
					eraseFirst = height[i];
					eraseSecond = height[j];
					for (int k = 0; k < 9; k++)
					{
						if (height[k] == eraseFirst || height[k] == eraseSecond)
						{
							continue;
						}
						answer.push_back(height[k]);
						answerAll = true;
					}
					break;
				}
			}
		}
		if (answerAll == true)
			break;
	}

	sort(answer.begin(), answer.end());

	for (auto p : answer)
	{
		cout << p << endl;
	}
		 
	return 0;
}